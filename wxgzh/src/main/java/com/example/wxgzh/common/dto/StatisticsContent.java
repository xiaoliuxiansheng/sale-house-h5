package com.example.wxgzh.common.dto;

public class StatisticsContent {
	private int onlineUsers;
	private int totalAccount;
	private String cpuPayload;
	private String memoryPayload;
	
	
	
	public int getOnlineUsers() {
		return onlineUsers;
	}
	public void setOnlineUsers(int onlineUsers) {
		this.onlineUsers = onlineUsers;
	}
	public int getTotalAccount() {
		return totalAccount;
	}
	public void setTotalAccount(int totalAccount) {
		this.totalAccount = totalAccount;
	}
	public String getCpuPayload() {
		return cpuPayload;
	}
	public void setCpuPayload(String cpuPayload) {
		this.cpuPayload = cpuPayload;
	}
	public String getMemoryPayload() {
		return memoryPayload;
	}
	public void setMemoryPayload(String memoryPayload) {
		this.memoryPayload = memoryPayload;
	}
	
	
	
	
	
}
